            name_re = re.search(r'(‡∏ô‡∏≤‡∏¢|‡∏ô‡∏≤‡∏á‡∏™‡∏≤‡∏ß|‡∏î\.‡∏ä\.|‡∏î\.‡∏ç\.)\s*([^\s\d]+)\s+([^\s\d]+)', full_text)
